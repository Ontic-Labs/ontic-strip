import { spawn } from "node:child_process";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";

const DEFAULT_OUT_PATH = "src/integrations/supabase/types.ts";
const args = process.argv.slice(2);

function getFlagValue(flagName) {
  const index = args.indexOf(flagName);
  if (index === -1) return undefined;
  return args[index + 1];
}

function hasFlag(flagName) {
  return args.includes(flagName);
}

async function getProjectIdFromSupabaseConfig() {
  try {
    const configPath = resolve("supabase/config.toml");
    const raw = await readFile(configPath, "utf8");
    const match = raw.match(/^project_id\s*=\s*"([^"]+)"/m);
    return match?.[1];
  } catch {
    return undefined;
  }
}

function run(command, commandArgs) {
  return new Promise((resolvePromise, rejectPromise) => {
    const child = spawn(command, commandArgs, {
      stdio: ["ignore", "pipe", "pipe"],
      env: process.env,
    });

    let stdout = "";
    let stderr = "";

    child.stdout.on("data", (chunk) => {
      stdout += String(chunk);
    });

    child.stderr.on("data", (chunk) => {
      stderr += String(chunk);
    });

    child.on("error", (error) => {
      rejectPromise(error);
    });

    child.on("close", (code) => {
      if (code === 0) {
        resolvePromise({ stdout, stderr });
        return;
      }
      rejectPromise(
        new Error(`${command} ${commandArgs.join(" ")} failed (${code})\n${stderr || stdout}`),
      );
    });
  });
}

async function main() {
  const outPath = resolve(getFlagValue("--out") || DEFAULT_OUT_PATH);
  const schema = getFlagValue("--schema") || process.env.SUPABASE_SCHEMA || "public";
  const checkOnly = hasFlag("--check");

  const explicitLocal = hasFlag("--local");
  const projectId =
    getFlagValue("--project-id") ||
    process.env.SUPABASE_PROJECT_ID ||
    (await getProjectIdFromSupabaseConfig());

  const sourceArgs = explicitLocal
    ? ["--local"]
    : projectId
      ? ["--project-id", projectId]
      : ["--local"];

  const cliArgs = [
    "--yes",
    "supabase",
    "gen",
    "types",
    "typescript",
    "--schema",
    schema,
    ...sourceArgs,
  ];

  console.log(`[supabase:types] Generating schema types (${sourceArgs.join(" ")})`);
  const { stdout } = await run("npx", cliArgs);

  if (!stdout.includes("export type Database")) {
    throw new Error("Supabase CLI output did not include expected Database types.");
  }

  await mkdir(dirname(outPath), { recursive: true });

  const banner =
    "// Auto-generated by scripts/gen-supabase-types.mjs\n// Do not edit this file manually.\n\n";
  const nextContent = `${banner}${stdout.trim()}\n`;

  let currentContent = "";
  try {
    currentContent = await readFile(outPath, "utf8");
  } catch {
    currentContent = "";
  }

  if (currentContent === nextContent) {
    console.log(`[supabase:types] No changes in ${outPath}`);
    return;
  }

  if (checkOnly) {
    console.error("[supabase:types] Types are out of date. Run: npm run supabase:types");
    process.exit(1);
  }

  await writeFile(outPath, nextContent, "utf8");
  console.log(`[supabase:types] Updated ${outPath}`);
}

main().catch((error) => {
  console.error("[supabase:types] Failed:", error instanceof Error ? error.message : String(error));
  process.exit(1);
});
